def main() -> None:
    args = _parse_args()
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df_ts = _load_timeseries(args.csv)
    _validate_minimum(df_ts)
    df_ts = _compute_xcom_bos_norm(df_ts)

    platform = _load_platform_labels(args.platform_xlsm)

    # Snapshots (onset + 300ms)
    snap_onset = _build_snapshot(
        df_ts,
        SnapshotSpec(name="xCOM_BOS_norm_onset", frame_expr=pl.col("platform_onset_local")),
    )
    snap_300 = _build_snapshot(
        df_ts,
        SnapshotSpec(
            name="xCOM_BOS_norm_300ms",
            frame_expr=(pl.col("platform_onset_local") + int(args.frames_300ms)),
        ),
    )

    snap = (
        # Keep all trials that have either onset or 300ms snapshot available.
        snap_onset.join(snap_300, on=TRIAL_KEYS, how="full", coalesce=True)
        .join(platform, on=TRIAL_KEYS, how="left")
    )

    # Keep only step/nonstep trials for group comparison.
    snap = snap.with_columns(pl.col("step_TF").cast(pl.Utf8, strict=False))
    usable = snap.filter(pl.col("step_TF").is_in(["step", "nonstep"]))

    # Convert to pandas for statsmodels + markdown output.
    analysis_df = usable.to_pandas()

    # Count summary
    counts = {
        "n_trials_usable": int(len(analysis_df)),
        "n_subjects_usable": int(pd.Series(analysis_df["subject"]).nunique(dropna=True)),
        "n_step": int((analysis_df["step_TF"] == "step").sum()),
        "n_nonstep": int((analysis_df["step_TF"] == "nonstep").sum()),
    }

    dv_cols: list[str] = ["xCOM_BOS_norm_onset", "xCOM_BOS_norm_300ms"]
    results = _fit_lmm(analysis_df, dv_cols=dv_cols, timeout_sec=int(args.timeout_sec))

    # Persist outputs under analysis/initial_posture_strategy_lmm (as requested)
    results_out_csv = out_dir / "tmp_raw_xcom_bos_step_vs_nonstep_lmm_results.csv"
    _write_utf8_sig_csv(results, results_out_csv)

    report_out_md = out_dir / "tmp_report_raw_xcom_bos_step_vs_nonstep.md"
    notes = [
        "No filtering applied (no meta_prefilter, no post-filter): dataset includes heterogeneous subjects/trials.",
        "Raw CSV generated with meta_prefilter disabled does not contain step_TF; labels are joined from platform sheet.",
        "Forceplate inertial subtract QC warnings may appear during batch export, but this analysis only uses xCOM/BOS (marker-based).",
    ]
    _write_report(
        out_path=report_out_md,
        input_csv=args.csv,
        frames_300ms=int(args.frames_300ms),
        counts=counts,
        results=results,
        notes=notes,
    )

    print(f"[OK] results: {results_out_csv}")
    print(f"[OK] report:  {report_out_md}")
    print(f"[INFO] usable trials: {counts}")


if __name__ == "__main__":
    main()
